use std::{
    convert::Infallible as Never,
    fmt,
    str::FromStr,
};
#[cfg(feature = "async-proto")] use async_proto::Protocol;

macro_rules! blocks {
    ($($variant:ident => $id:literal,)*) => {
        /// The [resource location](https://minecraft.wiki/w/Resource_location) of a block.
        #[allow(missing_docs)] // don't need to document every variant
        #[derive(Debug, Default, Clone, PartialEq, Eq, PartialOrd, Ord, Hash)]
        #[cfg_attr(feature = "async-proto", derive(Protocol), async_proto(as_string))]
        pub enum BlockId {
            #[default]
            Air,
            $($variant,)*
            Other(String),
        }

        impl FromStr for BlockId {
            type Err = Never;

            fn from_str(s: &str) -> Result<Self, Never> {
                Ok(match s {
                    "minecraft:air" | "minecraft:void_air" | "minecraft:cave_air" => Self::Air,
                    $($id => Self::$variant,)*
                    _ => Self::Other(s.to_owned()),
                })
            }
        }

        impl fmt::Display for BlockId {
            fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
                match self {
                    Self::Air => write!(f, "minecraft:air"),
                    $(Self::$variant => write!(f, $id),)*
                    Self::Other(id) => id.fmt(f),
                }
            }
        }
    };
}

